function [] = print_current_solution(cycle_data)
%% Print bounds
A = cycle_data.optimization.check_bounds;
[n, ~] = size(A);
fprintf('\n')
fprintf('|-----------------------------------------------------------------------------------|\n')
fprintf('|-------------------- Values and bounds of the design variables --------------------|\n')
fprintf('|-----------------------------------------------------------------------------------|\n')
fprintf('%31s %15s %15s %15s \n', A{1,1}, A{1,2}, A{1,3}, A{1,4});
for i = 2:n
    fprintf('%31s %15.4f %15.4f %15.4f \n', A{i,1}, A{i,2}, A{i,3}, A{i,4});
end
fprintf('|-----------------------------------------------------------------------------------|\n')


%% Print constraints
B = cycle_data.optimization.check_constraints;
[n, ~] = size(B);
fprintf('\n')
fprintf('|---------------------------------------------------------------------------------------------------|\n')
fprintf('|--------------------------------------- Problem constraints ---------------------------------------|\n')
fprintf('|---------------------------------------------------------------------------------------------------|\n')
fprintf('%31s %15s %18s %15s %12s \n', B{1,1}, B{1,2}, B{1,3}, B{1,4}, B{1,5});
for i = 2:n
    fprintf('%31s %15.4f %18.4f %15.4f %12s \n', B{i,1}, B{i,2}, B{i,3}, B{i,4}, B{i,5});
end
fprintf('|---------------------------------------------------------------------------------------------------|\n')

end